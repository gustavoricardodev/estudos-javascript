// O ES6 trouxe para o JavaScript o conceito de classes

// Classes nada mais são do que funções construtoras com uma sitaxe diferente, que de certa forma facilita a forma como as implementamos.

// Esse tipo de fenômeno é chamado de syntactic sugar.

// Segue um exemplo simples de classe:

class Veiculo {
  constructor() {}
  element() {}
}

// Com essa montagem básica de uma class, vemos que ela engloba duas funções, constructor e element.
