// Objetos são conjutos de variáveis e funções.

// Variáveis que se encontram dentro de objetos são chamadas de PROPRIEDADES.

// Funções que se encontram dentro de objetos são chamadas de MÉTODOS.

// Métodos e propriedades de um objeto são acessados por dot notation, ou seja, por um ponto.

const pessoa = {
    nome: 'Goku',
    idade: 19,
    profissao: 'Astronauta',
};

console.log(pessoa.nome);
console.log(pessoa.profissao);
