// for

// O forloop é a mais utilizada pois nos proporciona variáveis de controle.

// Sem estrutura de repetição:

console.log("Passo 1");
console.log("Passo 2");
console.log("Passo 3");
console.log("Passo 4");
console.log("Passo 5");

// Com for:

for (let contador = 1; contador <= 5; contador++) {
  console.log(`Passo ${contador}`);
}
