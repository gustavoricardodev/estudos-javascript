// do while:

// O do while é uma estrutura de repetição com teste lógico no final.

// Sem estrutura de repetição:

console.log(`Passo 1`);
console.log(`Passo 2`);
console.log(`Passo 3`);
console.log(`Passo 4`);
console.log(`Passo 5`);

// com do while:

let c = 1;

do {
    console.log(`Passo ${c}`);
    c++;
} while (c <= 5);
