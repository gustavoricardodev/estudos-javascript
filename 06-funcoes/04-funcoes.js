// Quando estamos utilizando funções é muito importante entender a diferença entre parâmetro e argumento.

// Ao criar uma função podemos definir parâmetros.

// Ao executar uma função podemos passar argumentos.

function areaQuadrado(lado) {
  return lado * lado;
}

const quadrado1 = areaQuadrado(5);

console.log(quadrado1);

// Lado é um parâmetro.

// 5 é um argumento.
